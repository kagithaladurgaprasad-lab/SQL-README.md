-- AGGREGRATION FUNCTIONS 
SELECT COUNT(DEPARTMENT) AS COUNTOFEMPLOYEES FROM EMPLOYEES
GROUP BY DEPARTMENT;

SELECT MAX(SALARY)AS MAXSALARY FROM EMPLOYEES;
SELECT MIN(SALARY) AS MINSALARY FROM EMPLOYEES;

SELECT AVG(SALARY) AS AVGSALARY,
SUM(SALARY) AS TOTALSALARY FROM EMPLOYEES
WHERE DEPARTMENT ='IT';

SELECT DEPARTMENT,SUM(SALARY)AS HEIGHTESTSALARY FROM EMPLOYEES
GROUP BY DEPARTMENT
HAVING HEIGHTESTSALARY>100000;

SELECT DEPARTMENT ,AVG(SALARY) AS AVSAL FROM EMPLOYEES
GROUP BY DEPARTMENT 
ORDER BY AVSAL DESC
LIMIT 1;

SELECT DEPARTMENT ,SUM(SALARY) AS TOTALSAL FROM EMPLOYEES
GROUP BY DEPARTMENT ;

SELECT DEPARTMENT,AVG(SALARY) AS AVGSA FROM EMPLOYEES
GROUP BY DEPARTMENT 
HAVING AVGSA >60000;




-- STRING FUNCTIONS 
SELECT CONCAT(FIRSTNAME, " ", LASTNAME)AS FULLNAME FROM EMPLOYEES; 

SELECT UPPER( CONCAT(FIRSTNAME, " ", LASTNAME))AS EMPLOYENAMES,
LOWER(DEPARTMENT) AS DEPARTMENT FROM EMPLOYEES;

SELECT CONCAT(FIRSTNAME ,LASTNAME)AS FULLNAME,CHAR_LENGTH(CONCAT(FIRSTNAME,LASTNAME))AS TOTALLENGTH FROM EMPLOYEES;

SELECT SUBSTR(DEPARTMENT,1,3) AS SHORTFORM FROM  EMPLOYEES;

SELECT REPLACE(DEPARTMENT,'SALES','ACCOUNTS') AS UPDATED FROM EMPLOYEES;





-- DATE AND TIME 

