CREATE DATABASE EMPLOY;
USE EMPLOY;

CREATE TABLE EMPLOYEES(
EMPLOYEEID INT,
FIRSTNAME VARCHAR(50),
LASTNAME VARCHAR(50),
EMAIL VARCHAR(50),
PHONENUMBER VARCHAR(20) NOT NULL ,
HIRE_DATE DATE,
JOBTITLE VARCHAR(50),
DEPARTMENT VARCHAR(50),
SALARY INT CHECK (SALARY>0),
STATUS_ VARCHAR(20) DEFAULT "WORKING",
UNIQUE(PHONENUMBER),
UNIQUE(EMAIL),
PRIMARY KEY(PHONENUMBER,DEPARTMENT)
);


