-- CASE STATEMENT 

SELECT HIRE_DATE,
CASE 
WHEN YEAR(HIRE_DATE)<2020 THEN "EXPRIENCED"
WHEN YEAR(HIRE_DATE) BETWEEN 2020 AND 2022 THEN "MID-LEVEL"
WHEN YEAR(HIRE_DATE)>2022 THEN  "FRESHER"
END AS EXPRIENCELEVEL  FROM EMPLOYEES;

SELECT SALARY,
CASE 
WHEN SALARY<30000 THEN 'LOW'
WHEN SALARY <70000 THEN 'MEDIUM'
ELSE 'HIGH'
END AS SALARYLEVEL FROM EMPLOYEES;







-- ORDER OF EXCUTION 
SELECT DEPARTMENT ,COUNT(*) AS EX
FROM EMPLOYEES 
WHERE HIRE_DATE>'2022-01-01'
GROUP BY DEPARTMENT 
HAVING COUNT(*)>2
ORDER BY EX DESC
LIMIT 2 ;

