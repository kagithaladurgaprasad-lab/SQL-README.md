-- BEFORE DELETE 
DELIMITER //
CREATE TRIGGER BEFORE_DELETE_EMP
BEFORE DELETE ON EMPLOYEE FOR EACH ROW 
BEGIN 
IF OLD.PENDINGINVOICE >0 THEN 
SIGNAL SQLSTATE '45000'
SET MESSAGE_TEXT ='CANNOT DELETE EMPLOYEE IF PENDING INVOICE EXISTS';
END IF ;
END//
DELIMITER ;

-- AFTER DELETE
DELIMITER //
CREATE TRIGGER AFTER_DELETE_EMP
AFTER DELETE ON EMPLOYEE FOR EACH ROW 
BEGIN 
INSERT INTO EMPLOYEE_LOG(EMPID,ACTIONTYPE,OLDSALARY)
VALUES
(OLD.EMPID,'DELETED',OLD.SALARY);
END//
DELIMITER ;


